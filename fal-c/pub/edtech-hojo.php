<article class="article_wrapper">
<h3>導入いただくことにより期待できる効果</h3>

<ul class="dot_list">
<li>ICT利活用の促進やオンライン授業の実施のために、動画で学べるコンテンツなど先生方の指導内容の充実が期待できます。</li>
<li>動画を通じて『教えるプロ』のスキルや他校の先生方の授業を動画で　視聴し学ぶことができるので、授業改善の効果が期待できます。</li>
<li>特に新任・若手の先生方向けに、マナー研修・生徒(児童)対応研修・保護者対応研修・コンプライアンス研修・働き方改革研修・特別支援教育研修を在宅でも実施できるため、先生方の土台となる知識の習得が期待できます。</li>
</ul>

<ul class="attention_list">
<li>Find！アクティブラーナー学校導入版は「Edtech導入補助金」の交付申請を予定しているサービスです。</li>
<li>提供するサービスはFind！アクティブラーナー学校導入版Edtech導入補助金用特別パッケージです。</li>
</ul>
</article>

